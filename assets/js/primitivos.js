// Datos Primitivos

//String
let string = "Hola Mundo";
console.log(string);
console.log(typeof string);

//Number
let numero = 10;
console.log(numero);
console.log(typeof numero);

//Boolean
let boleano = true;
console.log(boleano);
console.log(typeof boleano);

//Undefined
let nombre;
console.log(nombre);
console.log(typeof nombre);


//Null
let nulo = null;
console.log(nulo);
console.log(typeof nulo);

//Symbol
let symbol1 = Symbol("Hola Mundo");
let symbol2 = Symbol("Hola Mundo");

console.log(symbol1);
console.log(symbol1 === symbol2);
